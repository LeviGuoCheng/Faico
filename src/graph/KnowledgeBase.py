    def GetVertex_two_hop_search(self, entity_id: str, relation1: str ,relation2: str) -> list[tuple[str, str, str]]:
        """
        Two-hop bidirectional search
        
        Args:
            entity_id: Starting entity ID
            relation1: Relation used in the first hop
            relation2: Relation used in the second hop
        
        Returns:
            list[tuple[str, str, str]]: Returns two lists containing results from the first and second hops
            First hop return format: [(mid_entity_id, is_forward)]
            Second hop return format: [(mid_entity_id, final_entity_id, is_forward)]
        """
        second_hop = []
        one_hop_triples = self.GetVertex(entity_id,[relation1])
        for one_hop_triple in one_hop_triples:
            two_hop_triples = self.GetVertex(one_hop_triple[2],[relation2])
            for head2,relation_2,tail2 in two_hop_triples:
                second_hop.append((head2,relation_2,tail2,None))
        return None,list(set(second_hop))
    def GetVertex_three_hop_search(self, entity_id: str, relation1: str ,relation2: str, relation3: str) -> list[tuple[str, str, str]]:
        """
        Three-hop bidirectional search
        
        Args:
            entity_id: Starting entity ID
            relation1: Relation used in the first hop
            relation2: Relation used in the second hop
            relation3: Relation used in the third hop
        
        Returns:
            list[tuple[str, str, str]]: Returns two lists containing results from the first and second hops
            First hop return format: [(mid_entity_id, is_forward)]
            Second hop return format: [(mid_entity_id, final_entity_id, is_forward)]
        """
        three_hop = []
        one_hop_triples = self.GetVertex(entity_id,[relation1])
        for one_hop_triple in one_hop_triples:
            two_hop_triples = self.GetVertex(one_hop_triple[2],[relation2])
            for two_hop_triple in two_hop_triples:
                three_hop_triples = self.GetVertex(two_hop_triple[2],[relation3])
                for three_hop_triple in three_hop_triples:
                    three_hop.append((one_hop_triple[0],one_hop_triple[1],one_hop_triple[2],two_hop_triple[0],two_hop_triple[1],two_hop_triple[2],three_hop_triple[0],three_hop_triple[1],three_hop_triple[2]))
        return list(set(three_hop))

